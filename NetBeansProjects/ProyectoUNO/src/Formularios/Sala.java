    private void unirmePartidaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_unirmePartidaMouseClicked
        try{
                        
            musicaFondo.Stop();
            String[] args = null;
            Tablero.hostname = this.hostname;
            Tablero.usuarioActual = this.usuarioActual;
            Tablero.main(args, this.usuarioActual, this.ipUnirme.getText());
            this.dispose();
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_unirmePartidaMouseClicked

