    public void agregarFichas(){
        FichaUsuario ficha;
        for(int i=0;i<listaDeUsuarios.size();i++){
            ficha = new FichaUsuario(((Usuario) listaDeUsuarios.get(i)),i+1);
            
            
            panelDeFichas.add(ficha);
            panelDeFichas.setVisible(true);
            ficha.setVisible(true);
            scrpListaDeFichas.setVisible(true);
            this.setVisible(true);
            this.repaint();
            scrpListaDeFichas.setViewportView(panelDeFichas);
            
            
        }
        
    }
    
    public void cargarListaDeUsuarios(){
        XML xml = new XML(this.hostname);
        xml.cargarUsuarios();
        xml.EcribirDocumento();
        
        this.listaDeUsuarios = xml.obtenerListaDeUsuarios();
        
        
        Collections.sort(listaDeUsuarios, new Comparator<Usuario>(){
            @Override
            public int compare(Usuario u1, Usuario u2) {
                return new Integer(u2.getPartidasGanadas()).compareTo(new Integer(u1.getPartidasGanadas()));
            }
	});
    }
    
    
    
    
    
    
    
